    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String employeeID = EID.getText();
        String employeeName = EName.getText();
        String employeeAge = EAge.getText();
        String employeeAdress = EAdress.getText();
        String employeeDesignation = EDesignation.getSelectedItem().toString();
        String employeeDepartment = EDepartment.getSelectedItem().toString();
        
         if (employeeID.isEmpty()  || employeeName.isEmpty() || employeeAge.isEmpty() || employeeAdress.isEmpty()) {
             JOptionPane.showMessageDialog(rootPane, "Fileds cannot be empty when adding a new user.", "Add a new Employee ", JOptionPane.WARNING_MESSAGE);
            return;
        }
        EmployeeClass user = new EmployeeClass(employeeName,employeeID,employeeAge,employeeAdress,employeeDesignation,employeeDepartment);
        if(user.addNewEmployee())
             JOptionPane.showMessageDialog(rootPane, "Employee added successfully.", "Add a new Employee", JOptionPane.INFORMATION_MESSAGE);
        else{
            JOptionPane.showMessageDialog(rootPane, "Something went wrong with the application. Will be fixing it soon.", "Add a new Employee", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

