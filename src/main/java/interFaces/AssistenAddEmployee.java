public void fillComboFormTextFile() {

        String filePath = "D:\\Files\\Designation.txt";
        File file = new File(filePath);

        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            Object[] lines = br.lines().toArray();

            for (int i = 0; i < lines.length; i++) {
                String line = lines[i].toString();
                EDesignation.addItem(line);
            }

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Sorry! something went wrong, " + ex, "designation names", JOptionPane.ERROR_MESSAGE);

        }
    }
    
